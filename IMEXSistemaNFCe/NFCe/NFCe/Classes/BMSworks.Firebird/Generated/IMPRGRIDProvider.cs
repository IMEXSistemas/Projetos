						dbCnn.Close();
				}

				throw ex;
			}
		}

		private static IMPRGRIDCollection ExecuteReader(ref IMPRGRIDCollection collection, ref FbDataReader dataReader, FbCommand dbCommand)
		{
			using (dataReader = dbCommand.ExecuteReader())
			{
				collection = new IMPRGRIDCollection();

				if (dataReader.HasRows)
				{
					while (dataReader.Read())
					{
						collection.Add(FillEntityObject(ref dataReader));
					}
				}

				if (!(dataReader.IsClosed))
				{
					dataReader.Close();
				}
				dataReader.Dispose();
			}

			return collection;
		}

		private static IMPRGRIDEntity FillEntityObject(ref FbDataReader DataReader) 
		{
			IMPRGRIDEntity entity = new IMPRGRIDEntity();

			FirebirdGetDbData getData = new FirebirdGetDbData();

							entity.IDIMPRGRID = getData.ConvertDBValueToInt32(DataReader, DataReader.GetOrdinal("IDIMPRGRID"));
			entity.NOMETELA = getData.ConvertDBValueToStringNullable(DataReader, DataReader.GetOrdinal("NOMETELA"));
			entity.NOMEGRID = getData.ConvertDBValueToStringNullable(DataReader, DataReader.GetOrdinal("NOMEGRID"));
			entity.CAMPOSSELECIONADOS = getData.ConvertDBValueToStringNullable(DataReader, DataReader.GetOrdinal("CAMPOSSELECIONADOS"));
			entity.FLAGAJUSTA = getData.ConvertDBValueToStringNullable(DataReader, DataReader.GetOrdinal("FLAGAJUSTA"));
			entity.FLAGMODOPAISAGEM = getData.ConvertDBValueToStringNullable(DataReader, DataReader.GetOrdinal("FLAGMODOPAISAGEM"));
			entity.FLAGEXIBIRDATA = getData.ConvertDBValueToStringNullable(DataReader, DataReader.GetOrdinal("FLAGEXIBIRDATA"));
			entity.IDFUNCIONARIO = getData.ConvertDBValueToInt32Nullable(DataReader, DataReader.GetOrdinal("IDFUNCIONARIO"));


			return entity;
		}
